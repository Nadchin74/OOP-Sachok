using lab3v15;

Console.WriteLine("--- Створення загону роботів ---");

// Створюємо список, що може зберігати будь-які об'єкти, успадковані від Robot
List<Robot> robots = new List<Robot>();

// Додаємо в список об'єкти дочірніх класів
robots.Add(new CleanerRobot("Воллі", 10, "Сектор 7G"));
robots.Add(new DeliveryRobot("R2-D2", 5, 25.5));
robots.Add(new CleanerRobot("Єва", 15, "Головний хол"));

Console.WriteLine("\n--- Демонстрація поліморфізму: робота загону ---");

// Завдяки поліморфізму ми можемо працювати з різними роботами однаково
foreach (Robot robot in robots)
{
    Console.WriteLine("----------------------------------");
    robot.ShowInfo(); // Викличеться перевизначений метод для кожного типу робота
    robot.Work();     // і тут теж
}

Console.WriteLine("\n--- Робота завершена. Демонстрація деструкторів ---");
// Деструктори будуть викликані автоматично при завершенні програми